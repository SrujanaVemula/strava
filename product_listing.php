        $(".accordian_content").first().slideDown();
        $(".accordian_title").click(function () {
            $(this).parent().toggleClass("active");
            $(this).parent().siblings().removeClass("active");
            $(this).parent().find(".accordian_content").slideToggle();
            $(this).parent().siblings().find(".accordian_content").slideUp();
        })

        $('.tab .tab_item').on('click', function () {
            $(".cmn_btn").removeClass('active');
            $(this).find(".cmn_btn").addClass('active');
            var getParent = $(this).parents('.tab_section');
            var getTabcontent = $(this).attr('data-tab');
            getParent.find('.tab_content_wrap .tabcontent').removeClass('active');
            getParent.find(getTabcontent).addClass('active');
            $('.product_slider').slick('refresh');
        })
